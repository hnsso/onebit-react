import { useState, useMemo, useContext } from "react";
import "./List.css";
import TodoItem from "./TodoItem";
import { TodoStateContext } from "../App";

// Íµ¨Ï°∞Î∂ÑÌï¥ Ìï†ÎãπÏúºÎ°ú propsÎ°ú Í∞ùÏ≤¥Î•º Î∞õÏïÑÏò¨ÏàòÏûàÎã§
const List = () => {
  const todos = useContext(TodoStateContext);
  const [search, setSearch] = useState("");

  const onChangeSearch = (e) => {
    setSearch(e.target.value);
  };

  const getFileteredData = () => {
    // searchÏóê "ÎπàÎ∞∞Ïó¥"Ïù¥Î©¥ todosÎ•º Î∞òÌôòÌïòÍ≥†
    if (search === "") {
      return todos;
    }
    // todosÏóê todoÍ∞Ä Ìè¨Ìï®ÎêòÏñ¥ÏûàÏúºÎ©¥ Ï∞æÏïÑÎÇ¥Í≤†Îã§
    return todos.filter((todo) =>
      todo.content.toLowerCase().includes(search.toLowerCase())
    );
  };

  const filteredTodos = getFileteredData();

  const { totalCount, doneCount, notDoneCount } = useMemo(() => {
    const totalCount = todos.length;
    const doneCount = todos.filter((todo) => todo?.isDone).length;
    const notDoneCount = totalCount - doneCount;

    return {
      totalCount,
      doneCount,
      notDoneCount,
    };
  }, [
    // ÏùòÏ°¥ÏÑ±Î∞∞Ïó¥ : deps
    todos,
  ]);

  return (
    <div className="List">
      <h4>Todo List üå±</h4>
      <div>
        <div>total : {totalCount}</div>
        <div>done : {doneCount}</div>
        <div>notDone : {notDoneCount}</div>
      </div>
      <input
        value={search}
        onChange={onChangeSearch}
        type="text"
        placeholder="Í≤ÄÏÉâÏñ¥Î•º ÏûÖÎ†•ÌïòÏÑ∏Ïöî"
      />
      <div className="todos_wrapper">
        {filteredTodos.map((todo) => {
          return <TodoItem key={todo.id} {...todo} />;
        })}
      </div>
    </div>
  );
};

export default List;
